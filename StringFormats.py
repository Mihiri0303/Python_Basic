# Simple String Format


# Single Quotation
print('Hello Sri Lanka!')

# Double Quotations
print("Hello Sri Lanka!")


# New Line
print("Hello I am Mihiri.\nHow are you?")


# Hide letters
print("Python Basic")

# Hide letter y
print("Py\bthon Basic")

# Hide letter s
print("Python Bas\bic")


# Customise string {}
sample = "Time is {0} - Today is {1} - Month is{2} - Year is {3}"

print(sample.format(12.12,"Monday","June",2021))

