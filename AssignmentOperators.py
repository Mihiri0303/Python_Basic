# Assignment Operators
# (=) Operator

# to assign a value

a = 100
print(a)  # >>> 100

a = 200
print(a)  # >>> 200

# (+=) Operator

a += 10

print(a)  # >>> 210

# (-=) Operator

a -= 20

print(a)  # >>> 190

# (*=) Operator

a *= 2

print(a)  # >>> 380

# (/=) Operator

a /= 2

print(a)  # >>> 190.0

# (//=) Operator

a //= 2

print(a)  # >>> 95.0

# (%=) Operator

a %= 3

print(a)  # >>> 2.0

# (**=) Operator

a **= 2

print(a)  # >>> 4.0











